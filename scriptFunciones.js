/* crear una funcion que nos indique por pantalla si un numero ingresado es par o impar, el programa se detiene cuando el usuario ingrese impar */
function parImpar(){
    do{
        var numero = prompt('ingresa un numero');
        var estado = true;
        if(numero % 2 ==1){
            estado == false;
        }
    }while (estado == true);
}
